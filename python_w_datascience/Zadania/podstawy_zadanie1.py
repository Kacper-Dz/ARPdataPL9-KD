# 1. Utwórz zmienne: x i y oraz przypisz do nich dowolne liczby
x = 3
y = 4
# 2. Wyświetl wszytskie operacje arytmetycczne z wykorzystaniem zmiennych
print("Wynik dodawania wynosi:", x + y)
print("Wynik odejmowania wynosi:", x - y)
print("Wynik mnożenia wynosi:", x * y)
print("Wynik dzielenia wynosi:", x / y)
print("Wynik potęgowania wynosi:", x ** y)
print("Wynik pierwiastkowania x wynosi:", x ** (1/2))
print("Wynik pierwiastkowania y wynosi:", y ** (1/2))
print("Reszta z dzielenia x przez y wynosi:", x % y)
print("Wynik dzielenia x przez y bez reszty wynosi:", x // y)