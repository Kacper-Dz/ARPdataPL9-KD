# Napisać program, który przyjmie od użytkownika dwie wartości zmiennoprzecinkowe (float)
num_a = float(input("Cześć, podaj proszę liczbę a: "))
num_b = float(input("Teraz podaj liczbę b: "))
# A nastepnie wyświetli ich: sumę, różnicę oraz iloczyn
print("Suma liczby a i b wynosi: ", num_a + num_b)
print("Różnica liczby a i b wynosi: ", num_a - num_b)
print("Iloczyn licz a i b wynosi: ", num_a * num_b)

